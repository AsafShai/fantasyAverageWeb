-- Phase 1: one row per team per run (estimated_final_* and variance_* per stat)
CREATE TABLE IF NOT EXISTS team_prediction (
    id SERIAL PRIMARY KEY,
    run_id INTEGER NOT NULL,
    team_id INTEGER NOT NULL,
    team_name VARCHAR(100),
    as_of_date DATE NOT NULL,
    projected_total_gp NUMERIC NOT NULL,
    estimated_final_fg_pct NUMERIC NOT NULL,
    variance_fg_pct NUMERIC NOT NULL,
    estimated_final_ft_pct NUMERIC NOT NULL,
    variance_ft_pct NUMERIC NOT NULL,
    estimated_final_three_pm NUMERIC NOT NULL,
    variance_three_pm NUMERIC NOT NULL,
    estimated_final_reb NUMERIC NOT NULL,
    variance_reb NUMERIC NOT NULL,
    estimated_final_ast NUMERIC NOT NULL,
    variance_ast NUMERIC NOT NULL,
    estimated_final_stl NUMERIC NOT NULL,
    variance_stl NUMERIC NOT NULL,
    estimated_final_blk NUMERIC NOT NULL,
    variance_blk NUMERIC NOT NULL,
    estimated_final_pts NUMERIC NOT NULL,
    variance_pts NUMERIC NOT NULL,
    nba_avg_pace NUMERIC,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_team_prediction_run_team UNIQUE (run_id, team_id)
);
